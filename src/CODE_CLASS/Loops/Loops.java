package CODE_CLASS.Loops;

public class Loops {
    public static void main(String[] args) {

        //For loop
        for (int i = 0; i < 100; i++) {
            System.out.println("For loop: "+i);
        }
        System.out.println("\n\n");

        //while loop
        int i = 0;
        while (i<100){
            System.out.println("While loop: "+i);
            i++;
        }
    }
}
